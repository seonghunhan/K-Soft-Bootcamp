/**********************************************************
 * EOF 제어 while 반복문을 사용해                         *
 * 키보드로 입력한 숫자들의 합을 구하는 프로그램            *
 **********************************************************/ 
#include <iostream>
using namespace std;

int main()  
{
  // 선언
  int sum = 0;
  int num;
  // 초기화하고 반복문 돌리기
  cout << "첫 번째 숫자를 입력하세요(종료하려면 EOF 입력): ";
  while(cin >> num)
  {
    sum = sum + num;
    cout << "다음 숫자를 입력하세요: ";   // 업데이트
  }
  // 출력
  cout << "합 = " << sum;
  return 0; 
}