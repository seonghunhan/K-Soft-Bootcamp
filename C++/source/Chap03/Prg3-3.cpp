#include <iostream>
using namespace std;

int main()
{
  // 변수 선언과 초기화
  int x = 4;
  int y = -10;
  // 변수 x에 양수 음수 연산자 적용
  cout << "x에 양수 연산자 적용하기: " << +x << endl; 
  cout << "x에 음수 연산자 적용하기: " << -x << endl; 
  // 변수 y에 양수 음수 연산자 적용
  cout << "y에 양수 연산자 적용하기: " << +y << endl; 
  cout << "y에 음수 연산자 적용하기: " << -y;  
  return 0;
}