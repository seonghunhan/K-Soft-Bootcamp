/************************************************************* 
 * 우선 순위가 다른 3개의 표현식 평가 순서                   *
 *************************************************************/
#include <iostream>
using namespace std;

int main()
{
  // 변수 하나 선언
  int result;
  // 표현식을 평가하고, 값을 변수 result에 저장
  result = 5 - 15 % 4;
  // 변수 result에 저장된 값 출력
  cout << "result에 저장된 값: " << result;   
  return 0;      
} 