/*********************************************************** 
 * 복합 할당 표현식을 몇 가지 보여주는 프로그램            *
 ***********************************************************/
#include <iostream>
using namespace std;

int main()
{
  // 5개의 변수 선언과 초기화
  int x = 20;
  int y = 30;
  int z = 40; 
  int t = 50;
  int u = 60;
  // 복합 할당 사용
  x += 5;
  y -= 3;
  z *= 10;
  t /= 8;
  u %= 7;
  // 결과
  cout << "x의 값: " << x << endl;
  cout << "y의 값: " << y << endl;
  cout << "z의 값: " << z << endl;
  cout << "t의 값: " << t << endl;
  cout << "u의 값: " << u;
  return 0;
}