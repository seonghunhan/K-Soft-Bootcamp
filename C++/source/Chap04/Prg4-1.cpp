/**************************************************************
 * if 조건문을 사용해서 절대값을 계산하고 출력하는 프로그램   *
 **************************************************************/
#include <iostream>
using namespace std;

int main()  
{
  // 변수 선언
  int number;
  // 입력받기
  cout << "정수를 입력하세요: ";
  cin >> number;
  // 절대값 찾기
  if(number < 0)
  {
    number = -number;
  }
  // 절대값 출력
  cout << "입력한 숫자의 절대값 = " << number;
  return 0;
} 