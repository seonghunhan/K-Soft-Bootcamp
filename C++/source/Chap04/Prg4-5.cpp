/***************************************************************************
 *입력받은 두 숫자가 왼쪽이 더 큰지, 같은지, 더 작은지 출력하는 프로그램   * 
 ***************************************************************************/ 
#include <iostream>
using namespace std;

int main()  
{
  // 변수 선언
  int num1, num2;
  // 입력받기
  cout << "첫 번째 숫자 입력: ";
  cin >> num1;
  cout << "두 번째 숫자 입력: ";
  cin >> num2;
  // if-else 조건문을 중첩해서 의사 결정
  if(num1 >=  num2)
  {
    if(num1 >  num2)
    {
      cout << num1 << " > " << num2;
    } 
    else 
    {
     cout << num1 << " == " << num2;
    } 
  } 
  else 
  {
    cout << num1 << " < " << num2;
  } 
  return 0;
}